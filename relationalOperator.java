package work;

public class relationalOperator {

	public static void main(String[] args) {
		int a = 25;
		int b = 23;
		System.out.println(a < b);
		System.out.println(a > b);
		System.out.println(a <= b);
		System.out.println(a >= b);
		System.out.println(a == b);
		System.out.println(a != b);

		System.out.println("next");
		int a1 = 10;
		int b1 = 10;

		System.out.println(a1 < b1);
		System.out.println(a1 > b1);
		System.out.println(a1 <= b1);
		System.out.println(a1 >= b1);
		System.out.println(a1 == b1);
		System.out.println(a1 != b1);

	}

}
