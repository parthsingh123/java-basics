package Worksheet;

public class switchloop {

	public static void main(String[] args) {
int number = 45;
switch (number){
case 25:
System.out.println("25");
break;

case 35:
	System.out.println("35");
break;

case 45:
	System.out.println("45");
	break;
	
case 55:
	System.out.println("55");
	break;
}
	}

}
