package Worksheet;

public class TernaryOperator {

	public static void main(String[] args) {

		int a = 5;
		int b = 7;
		int c = 9;
		System.out.println((a<b) ? a:b);
		System.out.println((a>b) ? a:b);
		System.out.println((a+b <=c) ? c:a);
		System.out.println((a+b >=c) ? c:a);
		
	}

}
